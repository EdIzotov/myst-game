package com.intersog.mysteriousnumber.client.misc;

public class ModString {
    public static String tabString(String string) {
        return "\t" + string;
    }
    public static String lnBeforeString(String string) {
        return "\n\n\n\n" + string;
    }
    public static String lnAfterString(String string) {
        return string + "\n\n\n\n";
    }
}
